// ── Clear ───────────────────────────────────────────────────────────────────
function handleClear() {
    if (!confirm('Clear all per-school forecast data and the uploaded dataset?')) return;
    const msg = document.getElementById('clearMsg');
    msg.innerHTML = "<div style='color:#003D99; padding:8px;'>⏳ Clearing data…</div>";
    const fd  = new FormData();
    fd.append('clear', '1');
    fd.append('ajax', '1');

    fetch('school_forecast.php', { method:'POST', body: fd })
        .then(r => {
            if (!r.ok) throw new Error('Server error: ' + r.status);
            return r.text();
        })
        .then(data => {
            msg.innerHTML = data;
            // Only reload if the response confirms success
            if (data.includes('✅')) {
                setTimeout(() => window.location.href = 'school_forecast.php', 1200);
            }
        })
        .catch(e  => { msg.innerHTML = "<div class='error'>❌ " + e.message + "</div>"; });
}

