// ── Upload ──────────────────────────────────────────────────────────────────
function handleSchoolUpload() {
    const file = fileInput.files[0];
    const msg  = document.getElementById('uploadMsg');
    if (!file) { msg.innerHTML = "<div class='error'>❌ Please select a file first.</div>"; return; }

    const fd = new FormData();
    fd.append('file_school', file);
    fd.append('ajax', '1');
    msg.innerHTML = "<div style='color:#003D99; padding:8px;'>⏳ Uploading…</div>";

    fetch('school_forecast.php', { method:'POST', body: fd })
        .then(r => r.text())
        .then(data => {
            msg.innerHTML = data;
            fileInput.value = '';
            fileLabel.textContent = 'No file selected';

            // If upload succeeded, update the status badge immediately without a page reload
            if (data.includes('✅')) {
                const badge    = document.getElementById('datasetStatusBadge');
                const fileInfo = document.getElementById('datasetFileInfo');
                if (badge) {
                    badge.className = 'status-badge badge-ok';
                    badge.textContent = '✅ Dataset loaded';
                }
                if (fileInfo) {
                    fileInfo.style.display = '';
                }
                // Also enable the Generate button visually
                const genBtn = document.getElementById('generateBtn');
                if (genBtn) genBtn.disabled = false;
            }
        })
        .catch(e  => { msg.innerHTML = "<div class='error'>❌ " + e.message + "</div>"; });
}

// ── Generate forecast ───────────────────────────────────────────────────────
function handleGenerate() {
    const msg = document.getElementById('generateMsg');
    msg.innerHTML = "<div style='color:#003D99; padding:8px;'>⏳ Running forecast for all schools… this may take a moment.</div>";

    const fd = new FormData();
    fd.append('run', '1');
    fd.append('ajax', '1');

    fetch('school_forecast.php', { method:'POST', body: fd })
        .then(r => r.text())
        .then(data => {
            msg.innerHTML = data;
