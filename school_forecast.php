// ── Table: filter by year + search ─────────────────────────────────────────
function filterTable() {
    const year   = document.getElementById('yearFilter')?.value || '';
    const search = (document.getElementById('schoolSearch')?.value || '').toLowerCase();
    document.querySelectorAll('#schoolTbody tr').forEach(row => {
        // Compare as integers to avoid any string/float mismatch
        const matchYear   = !year || parseInt(row.dataset.year) === parseInt(year);
        const matchSearch = !search || row.textContent.toLowerCase().includes(search);
        row.style.display = (matchYear && matchSearch) ? '' : 'none';
    });
}

// ── Table: sort ─────────────────────────────────────────────────────────────
let sortDir = {};
function sortTable(col) {
    const tbody = document.getElementById('schoolTbody');
    if (!tbody) return;
    const rows = Array.from(tbody.querySelectorAll('tr'));
    const asc  = !sortDir[col];
    sortDir = {};
    sortDir[col] = asc;

    rows.sort((a, b) => {
        const at = a.cells[col]?.innerText.replace(/[,–]/g,'').trim() || '';
        const bt = b.cells[col]?.innerText.replace(/[,–]/g,'').trim() || '';
        const an = parseFloat(at), bn = parseFloat(bt);
        if (!isNaN(an) && !isNaN(bn)) return asc ? an - bn : bn - an;
        return asc ? at.localeCompare(bt) : bt.localeCompare(at);
    });
    rows.forEach(r => tbody.appendChild(r));
}
